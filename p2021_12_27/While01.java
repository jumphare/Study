package p2021_12_27;

public class While01 {

	public static void main(String[] args) {
		// while문으로 사랑해요 열 번 출력
		
		int num=1; //while문 밖에 초기값 지정
		while(num<=10) { //조건식
			System.out.println("사랑해요"+num);
			num++; //while문 내에 증감식 필수  (아니면 무한루프함)
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
